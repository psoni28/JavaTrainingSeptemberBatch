package operatorsPack;

public class ArithmaticOperatorDemo {

	public static void main(String[] args) {

		int a = 10, b = 5, res;

		res = a + b;

		System.out.println("addition = " + res);

		res = a - b;

		System.out.println("minus = " + res);

		res = a * b;

		System.out.println("multiply = " + res);

		res = a / b;

		System.out.println("divide = " + res);
		res = a % b;

		System.out.println("mod = " + res);

	}

}
